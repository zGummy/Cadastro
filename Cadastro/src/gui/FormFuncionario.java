    public void sincronizaTabela() {
        // Carregar a lista de editoras na tabela
        DefaultTableModel model = 
                (DefaultTableModel) tblFuncionario.getModel();
        
         // Reseta o modelo da tabela
        model.setRowCount(0);
        
        SimpleDateFormat formatador = new SimpleDateFormat("dd/MM/yyyy");
        
        for (Funcionario funcionario : funcionarioPesquisa) {
            Object[] linha = {funcionario.getId(), 
                funcionario.getNome(),
                formatador.format(funcionario.getDataNascimento().getTime()),
                funcionario.getCpf(),
                funcionario.getDataCadastro()};
            
            model.addRow(linha);
        }
    }

