    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        
        try {
            Funcionario ultimoFuncionario = BancoDados.listaFuncionario.get(
                    BancoDados.listaFuncionario.size() - 1);
            
            SimpleDateFormat formatador = new SimpleDateFormat("dd/MM/yyyy");
            
            Integer proximoId = ultimoFuncionario.getId() + 1;
            
            String nomeFuncionario = txtNome.getText();
            
            GregorianCalendar dataNascimento = new GregorianCalendar();
            GregorianCalendar dataCadastro = new GregorianCalendar();
            
            dataNascimento.setTime(formatador.parse(txtDtNasc.getText()));
            dataCadastro.setTime(formatador.parse(txtDtCadastro.getText()));
            
            String cpf = txtCpf.getText();
            String funcao = txtFuncao.getText();
            String setor = txtSetor.getText();
            
            //Long telefone = Long.parseLong(txtTelefone.getText());
            
            Funcionario funcionario = new Funcionario(proximoId,
                    nomeFuncionario,
                    dataNascimento,
                    cpf,
                    dataCadastro,
                    funcao,
                    setor);
            
            BancoDados.listaFuncionario.add(funcionario);
            
            JOptionPane.showMessageDialog(null,
                    "Registro cadastrado com sucesso.");
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null,
                    "Formato de data inválido.");
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
                // Verificar critérios de pesquisa
        if (!txtPesquisaCodigo.getText().isEmpty()) {
            
            Integer idPesquisa = Integer.parseInt(
                txtPesquisaCodigo.getText());
            
            Funcionario funcionario = BancoDados.buscaFuncionarioPorId(idPesquisa);
                       
            if (funcionario == null) {
                JOptionPane.showMessageDialog(null,
                        "Nenhum registro encontrado.");
            } else {
                funcionarioPesquisa = new ArrayList<>();
                funcionarioPesquisa.add(funcionario);
            }
            
            sincronizaTabela();
            
        } else if (!txtPesquisaNome.getText().isEmpty()) {
            
            String nomePesquisa = txtPesquisaNome.getText();
            
            funcionarioPesquisa = 
                    BancoDados.buscaFuncionarioPorNome(nomePesquisa);
            
            if (funcionarioPesquisa.isEmpty()) {
                JOptionPane.showMessageDialog(null,
                        "Nenhum registro encontrado.");
            }
            
            sincronizaTabela();
            
        } else {
            JOptionPane.showMessageDialog(null, 
                    "É necessário informar o código ou o nome.");
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void tblFuncionarioKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tblFuncionarioKeyPressed
        if (evt.getKeyCode() == 127) {

            if (tblFuncionario.getSelectedRowCount() != 1) {
                JOptionPane.showMessageDialog(null,
                    "É necessário selecionar 1 registro apenas");
                return;
            }

            int opcao = JOptionPane.showConfirmDialog(null,
                "Deseja realmente excluir o registro?");

            if (opcao == JOptionPane.YES_OPTION) {

                int linhaSelecionada = tblFuncionario.getSelectedRow();

                Integer id = (Integer) tblFuncionario.getModel()
                .getValueAt(linhaSelecionada, 0);

                BancoDados.deleteFuncionario(id);

                // Remove o Funcionario da pesquisa
                for (Funcionario funcionario : funcionarioPesquisa) {
                    if (funcionario.getId().equals(id)) {
                        funcionarioPesquisa.remove(funcionario);
                        break;
                    }
                }

                sincronizaTabela();

            }

        }
    }//GEN-LAST:event_tblFuncionarioKeyPressed

