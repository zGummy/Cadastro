    @Override
    public void tableChanged(TableModelEvent tme) {
        if (tme.getType() == TableModelEvent.UPDATE) {
            try {
                int linhaAlterada = tme.getFirstRow();
                
                SimpleDateFormat formatador = new SimpleDateFormat("dd/MM/yyyy");
                
                // Recupera as informações de alteração
                Integer id = (Integer) tblFuncionario.getModel()
                        .getValueAt(linhaAlterada, 0);
                
                String nome = (String) tblFuncionario.getModel()
                        .getValueAt(linhaAlterada, 1);
                
                GregorianCalendar dataNascimento = new GregorianCalendar();
                GregorianCalendar dataCadastro = new GregorianCalendar();
                
                dataNascimento.setTime(formatador.parse((String) tblFuncionario.getModel()
                        .getValueAt(linhaAlterada, 2)));
                
                String cpf = (String) tblFuncionario.getModel()
                        .getValueAt(linhaAlterada, 3);
                
                 dataCadastro.setTime(formatador.parse((String) tblFuncionario.getModel()
                        .getValueAt(linhaAlterada, 4)));                
                
                
                 String funcao = (String) tblFuncionario.getModel()
                        .getValueAt(linhaAlterada, 5);
                 
                  String setor = (String) tblFuncionario.getModel()
                        .getValueAt(linhaAlterada, 6);
                
               // Long telefone = (Long) tblFuncionario.getModel().getValueAt(linhaAlterada, 4);
                
                BancoDados.updateFuncionario(id, nome, dataNascimento, cpf, dataCadastro,funcao,setor);
            } catch (ParseException ex) {
                Logger.getLogger(FormFuncionario.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }

    
